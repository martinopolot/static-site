import sys
import os

# Add the src directory to the Python path
sys.path.append(os.path.abspath(os.path.dirname(__file__)))


import unittest
from markdown_utils import extract_markdown_images, extract_markdown_links  

class TestMarkdownExtraction(unittest.TestCase):

    def test_extract_markdown_images(self):
        text = "This is text with an ![image](https://i.imgur.com/zjjcJKZ.png)"
        matches = extract_markdown_images(text)
        self.assertListEqual([("image", "https://i.imgur.com/zjjcJKZ.png")], matches)

    def test_multiple_images(self):
        text = "![img1](url1) text ![img2](url2)"
        matches = extract_markdown_images(text)
        self.assertListEqual([("img1", "url1"), ("img2", "url2")], matches)

    def test_extract_markdown_links(self):
        text = "Here is a [link](https://example.com)"
        matches = extract_markdown_links(text)
        self.assertListEqual([("link", "https://example.com")], matches)

    def test_multiple_links(self):
        text = "[one](url1) and [two](url2)"
        matches = extract_markdown_links(text)
        self.assertListEqual([("one", "url1"), ("two", "url2")], matches)

    def test_links_ignore_images(self):
        text = "Here is ![an image](image_url) and [a link](link_url)"
        matches = extract_markdown_links(text)
        self.assertListEqual([("a link", "link_url")], matches)

    def test_images_ignore_links(self):
        text = "Here is ![image](image_url) and [link](link_url)"
        matches = extract_markdown_images(text)
        self.assertListEqual([("image", "image_url")], matches)

